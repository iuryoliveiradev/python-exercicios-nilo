# Exercício 3.3 - Exibe a soma de 2 números inteiros informados

# Solicitando os números
valor_01 = int(input("Digite um número inteiro: "))
valor_02 = int(input("Digite outro número inteiro: "))

# Calculando a soma
soma = valor_01 + valor_02

# Exibindo o resultado
print(f"A soma de {valor_01} + {valor_02} é igual a {soma}")