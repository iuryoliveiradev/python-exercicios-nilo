# Exercício 2.7 - Demonstra equivalência entre expressões usando propriedades da multiplicação e divisão

# Expressão com números decimais
resultado_decimal = 0.2 * 6 + 8 * 0.3 + 7 * 0.5

# Expressão equivalente com inteiros e divisão por 100
resultado_equivalente = (20 * 6 + 8 * 30 + 7 * 50) / 100

# Exibindo os dois resultados
print(f"Resultado com decimais: {resultado_decimal}")
print(f"Resultado equivalente com inteiros e divisão: {resultado_equivalente}")