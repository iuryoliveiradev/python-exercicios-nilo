# Exercício 2.6 - Calcula o aumento de 15% para um salário de R$750

salario = 750
porcentagem_aumento = 15

# Calcula o valor do aumento e o novo salário
valor_aumento = salario * porcentagem_aumento / 100
novo_salario = salario + valor_aumento

# Exibe o resultado formatado
print(f"O salário de R${salario} teve um aumento de R${valor_aumento} ({porcentagem_aumento}%), passando a ser R${novo_salario}.")