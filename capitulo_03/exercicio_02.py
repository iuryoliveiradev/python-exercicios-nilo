# Exercício 3.2 - Verifica se o aluno foi aprovado com base nas notas individuais

# Notas do aluno em cada matéria
materia1 = 9
materia2 = 5
materia3 = 10

# Verificando se todas as notas são maiores ou iguais a 7
if materia1 >= 7 and materia2 >= 7 and materia3 >= 7:
    print("O aluno foi aprovado")
else:
    print("O aluno foi reprovado")