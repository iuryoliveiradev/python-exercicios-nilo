# Exercício 3.1 - Determina se a pessoa deve pagar imposto ou não

# Definindo salário de 2 pessoas fictícias (Ana e Brenda)
salario_ana = 1200.00
salario_brenda = 1350.00

# Para pagar imposto o salário deve ser maior que R$ 1.200,00
if salario_ana > 1200:
    print("Ana terá que pagar imposto!")
else:
    print("Ana não pagará imposto!")

if salario_brenda > 1200:
    print("Brenda terá que pagar imposto!")
else:
    print("Brenda não pagará imposto!")